package MekushBasics;

public class RelationalOperator {
    public static void main(String[] args) {
        System.out.println(5>3);
        System.out.println(5<3);
        System.out.println(5<=3);
        System.out.println(5>=3);
        boolean x=(10>5);
        System.out.println(x);
    }
}
