package MekushBasics;

public class Typecasting {
    public static void main(String[] args) {
        char kush = 'k';
        System.out.println(kush);
        int x = (int)kush;
        System.out.println(x);
        char ch = 'b';
        int y = ch;
        System.out.println(y);
        System.out.println(ch+3);
        System.out.println(ch+ch);


    }
}

