package MekushBasics;

public class ArithmeticOperators {
   public static void main(String[] args) {
       int x=45;
       int y=7;
       System.out.println(x+y);
       System.out.println(x-y);
       System.out.println(x*y);
       System.out.println(x/y);
       double a=45;
       double b=7;
       System.out.println(a+b);
       System.out.println(a-b);
       System.out.println(a*b);
       System.out.println(a/b);

    }
}
