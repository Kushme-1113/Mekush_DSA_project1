package MekushBasics;

public class incrementanddecOper {
    public static void main(String[] args){
       int x=10;
       System.out.println(x);
       System.out.println(x++);
       System.out.println(x);

       int y=9;
       System.out.println(y);
       System.out.println(++y);

       int a=10;
       int b=a++;
       System.out.println(a+" "+b);

    }
}
