package MekushArrays;

public class ForEach {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4,5};
        for(int a:arr){   // For Each loop it is used for display the array we not change the specific element of array,we change the whole array from this ..
            System.out.print(a+" ");
        }
    }
}
